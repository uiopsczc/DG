namespace DG
{
	public class FPInterpolationCircle : FPInterpolation
	{

		public override FP Apply(FP a)
		{
			if (a <= 0.5f)
			{
				a *= 2;
				return (1 - FPMath.Sqrt(1 - a * a)) / 2;
			}
			a = a - 1;
			a *= 2;
			return (FPMath.Sqrt(1 - a * a) + 1) / 2;
		}

	}
}

