namespace DG
{
    public class FPInterpolationPow2OutInverse : FPInterpolation
    {
        public override FP Apply(FP a)
        {
            if (a < FPMath.EPSILION) return 0;
            if (a > 1) return 1;
            return 1 - FPMath.Sqrt(-(a - 1));
        }
    }
}